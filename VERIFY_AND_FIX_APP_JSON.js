const fs = require('fs');
const path = require('path');

console.log('üîç VERIFICATION & CORRECTION COMPL√àTE APP.JSON');
console.log('‚ïê'.repeat(80));

const appJsonPath = './app.json';
const appJson = JSON.parse(fs.readFileSync(appJsonPath, 'utf8'));

let issues = [];
let fixes = 0;

// ============================================================================
// 1. V√âRIFICATION DRIVERS - MANUFACTURER IDS SYNC
// ============================================================================
console.log('\nüìã 1. V√âRIFICATION SYNC DRIVERS AVEC SOURCES...\n');

appJson.drivers.forEach((driver, index) => {
  const driverId = driver.id;
  const driverPath = `./drivers/${driverId}`;
  const composePath = `${driverPath}/driver.compose.json`;
  
  if (fs.existsSync(composePath)) {
    const composeData = JSON.parse(fs.readFileSync(composePath, 'utf8'));
    
    // Compare manufacturerName
    if (composeData.zigbee?.manufacturerName) {
      const sourceManufacturers = composeData.zigbee.manufacturerName;
      const appManufacturers = driver.zigbee?.manufacturerName || [];
      
      const missing = sourceManufacturers.filter(m => !appManufacturers.includes(m));
      
      if (missing.length > 0) {
        issues.push({
          driver: driverId,
          type: 'missing_manufacturers',
          details: missing
        });
        
        console.log(`   ‚ö†Ô∏è  ${driverId}: ${missing.length} manufacturer IDs manquants`);
        missing.forEach(m => console.log(`      - ${m}`));
        
        // FIX: Add missing manufacturers
        if (!driver.zigbee) driver.zigbee = {};
        if (!driver.zigbee.manufacturerName) driver.zigbee.manufacturerName = [];
        
        missing.forEach(m => {
          if (!driver.zigbee.manufacturerName.includes(m)) {
            driver.zigbee.manufacturerName.push(m);
            fixes++;
          }
        });
        
        console.log(`      ‚úÖ Ajout√©s √† app.json`);
      }
    }
    
    // Compare productId
    if (composeData.zigbee?.productId) {
      const sourceProducts = composeData.zigbee.productId;
      const appProducts = driver.zigbee?.productId || [];
      
      const missingProducts = sourceProducts.filter(p => !appProducts.includes(p));
      
      if (missingProducts.length > 0) {
        issues.push({
          driver: driverId,
          type: 'missing_products',
          details: missingProducts
        });
        
        console.log(`   ‚ö†Ô∏è  ${driverId}: ${missingProducts.length} product IDs manquants`);
        missingProducts.forEach(p => console.log(`      - ${p}`));
        
        // FIX: Add missing products
        if (!driver.zigbee.productId) driver.zigbee.productId = [];
        
        missingProducts.forEach(p => {
          if (!driver.zigbee.productId.includes(p)) {
            driver.zigbee.productId.push(p);
            fixes++;
          }
        });
        
        console.log(`      ‚úÖ Ajout√©s √† app.json`);
      }
    }
  }
  
  // Progress
  if ((index + 1) % 20 === 0 || index === appJson.drivers.length - 1) {
    process.stdout.write(`\r   Progress: ${index + 1}/${appJson.drivers.length} drivers v√©rifi√©s...`);
  }
});

console.log('\n');

// ============================================================================
// 2. V√âRIFICATION CAPABILITIES VS ENERGY.BATTERIES
// ============================================================================
console.log('\nüîã 2. V√âRIFICATION ENERGY.BATTERIES...\n');

appJson.drivers.forEach(driver => {
  const capabilities = driver.capabilities || [];
  
  if (capabilities.includes('measure_battery')) {
    if (!driver.energy || !driver.energy.batteries || driver.energy.batteries.length === 0) {
      // Skip AC powered devices
      if (driver.id.includes('_ac') || driver.id.includes('plug') || driver.id.includes('socket')) {
        return;
      }
      
      issues.push({
        driver: driver.id,
        type: 'missing_batteries',
        details: 'measure_battery without energy.batteries'
      });
      
      console.log(`   ‚ö†Ô∏è  ${driver.id}: measure_battery sans energy.batteries`);
      
      // FIX: Add default batteries
      let batteries = ['CR2032'];
      
      if (driver.id.includes('motion') || driver.id.includes('sensor')) {
        batteries = ['CR2032', 'CR2450', 'AA'];
      } else if (driver.id.includes('smoke') || driver.id.includes('co_') || driver.id.includes('gas')) {
        batteries = ['CR123A', 'AA'];
      } else if (driver.id.includes('door') || driver.id.includes('window')) {
        batteries = ['CR2032', 'AAA'];
      }
      
      if (!driver.energy) driver.energy = {};
      driver.energy.batteries = batteries;
      fixes++;
      
      console.log(`      ‚úÖ Ajout√©: [${batteries.join(', ')}]`);
    }
  }
});

// ============================================================================
// 3. V√âRIFICATION FLOW CARDS CONSISTENCY
// ============================================================================
console.log('\nüé® 3. V√âRIFICATION FLOW CARDS...\n');

if (!appJson.flow) {
  issues.push({
    type: 'missing_flow',
    details: 'No flow object in app.json'
  });
  console.log('   ‚ö†Ô∏è  Aucun flow object dans app.json');
  appJson.flow = { triggers: [], conditions: [], actions: [] };
  fixes++;
  console.log('      ‚úÖ Flow object cr√©√©');
}

const flowStats = {
  triggers: (appJson.flow.triggers || []).length,
  conditions: (appJson.flow.conditions || []).length,
  actions: (appJson.flow.actions || []).length
};

console.log(`   ‚úÖ Triggers: ${flowStats.triggers}`);
console.log(`   ‚úÖ Conditions: ${flowStats.conditions}`);
console.log(`   ‚úÖ Actions: ${flowStats.actions}`);

// ============================================================================
// 4. V√âRIFICATION SETTINGS FORMAT
// ============================================================================
console.log('\n‚öôÔ∏è  4. V√âRIFICATION SETTINGS...\n');

if (appJson.settings) {
  appJson.settings.forEach((setting, idx) => {
    // Check required fields
    if (!setting.id) {
      issues.push({
        type: 'invalid_setting',
        index: idx,
        details: 'Missing id'
      });
      console.log(`   ‚ö†Ô∏è  Setting #${idx}: id manquant`);
    }
    
    if (!setting.type) {
      issues.push({
        type: 'invalid_setting',
        index: idx,
        details: 'Missing type'
      });
      console.log(`   ‚ö†Ô∏è  Setting #${idx}: type manquant`);
    }
    
    if (!setting.title) {
      issues.push({
        type: 'invalid_setting',
        index: idx,
        details: 'Missing title'
      });
      console.log(`   ‚ö†Ô∏è  Setting #${idx}: title manquant`);
    }
    
    // Check dropdown values have title
    if (setting.type === 'dropdown' && setting.values) {
      setting.values.forEach((val, vIdx) => {
        if (!val.title) {
          issues.push({
            type: 'invalid_dropdown_value',
            setting: setting.id,
            index: vIdx,
            details: 'Missing title in dropdown value'
          });
          console.log(`   ‚ö†Ô∏è  Setting ${setting.id}: dropdown value #${vIdx} sans title`);
        }
      });
    }
  });
  
  console.log(`   ‚úÖ ${appJson.settings.length} settings v√©rifi√©s`);
} else {
  console.log('   ‚ö†Ô∏è  Aucun settings dans app.json');
}

// ============================================================================
// 5. V√âRIFICATION VERSION & METADATA
// ============================================================================
console.log('\nüì¶ 5. V√âRIFICATION METADATA...\n');

if (!appJson.version) {
  issues.push({ type: 'missing_version' });
  console.log('   ‚ö†Ô∏è  Version manquante');
} else {
  console.log(`   ‚úÖ Version: ${appJson.version}`);
}

if (!appJson.sdk || appJson.sdk !== 3) {
  issues.push({ type: 'invalid_sdk' });
  console.log(`   ‚ö†Ô∏è  SDK: ${appJson.sdk} (devrait √™tre 3)`);
} else {
  console.log(`   ‚úÖ SDK: ${appJson.sdk}`);
}

if (!appJson.compatibility) {
  issues.push({ type: 'missing_compatibility' });
  console.log('   ‚ö†Ô∏è  Compatibility manquante');
} else {
  console.log(`   ‚úÖ Compatibility: ${appJson.compatibility}`);
}

// ============================================================================
// 6. V√âRIFICATION IMAGES PATHS
// ============================================================================
console.log('\nüñºÔ∏è  6. V√âRIFICATION IMAGES PATHS...\n');

if (appJson.images) {
  ['small', 'large', 'xlarge'].forEach(size => {
    if (!appJson.images[size]) {
      issues.push({ type: 'missing_image', size });
      console.log(`   ‚ö†Ô∏è  Image ${size} manquante`);
    } else {
      const imagePath = `.${appJson.images[size]}`;
      if (!fs.existsSync(imagePath)) {
        issues.push({ type: 'image_not_found', path: imagePath });
        console.log(`   ‚ö†Ô∏è  Image non trouv√©e: ${imagePath}`);
      } else {
        console.log(`   ‚úÖ Image ${size}: ${appJson.images[size]}`);
      }
    }
  });
}

// ============================================================================
// 7. V√âRIFICATION DUPLICATES MANUFACTURER IDS
// ============================================================================
console.log('\nüîç 7. V√âRIFICATION DUPLICATES MANUFACTURER IDS...\n');

appJson.drivers.forEach(driver => {
  if (driver.zigbee?.manufacturerName) {
    const unique = [...new Set(driver.zigbee.manufacturerName)];
    const duplicates = driver.zigbee.manufacturerName.length - unique.length;
    
    if (duplicates > 0) {
      issues.push({
        driver: driver.id,
        type: 'duplicate_manufacturers',
        count: duplicates
      });
      console.log(`   ‚ö†Ô∏è  ${driver.id}: ${duplicates} duplicates trouv√©s`);
      
      // FIX: Remove duplicates
      driver.zigbee.manufacturerName = unique;
      fixes++;
      console.log(`      ‚úÖ Duplicates supprim√©s`);
    }
  }
  
  if (driver.zigbee?.productId) {
    const unique = [...new Set(driver.zigbee.productId)];
    const duplicates = driver.zigbee.productId.length - unique.length;
    
    if (duplicates > 0) {
      issues.push({
        driver: driver.id,
        type: 'duplicate_products',
        count: duplicates
      });
      console.log(`   ‚ö†Ô∏è  ${driver.id}: ${duplicates} product ID duplicates`);
      
      // FIX: Remove duplicates
      driver.zigbee.productId = unique;
      fixes++;
      console.log(`      ‚úÖ Duplicates supprim√©s`);
    }
  }
});

if (issues.filter(i => i.type === 'duplicate_manufacturers' || i.type === 'duplicate_products').length === 0) {
  console.log('   ‚úÖ Aucun duplicate trouv√©');
}

// ============================================================================
// SAVE FIXED APP.JSON
// ============================================================================
if (fixes > 0) {
  console.log(`\nüíæ SAUVEGARDE APP.JSON...`);
  fs.writeFileSync(appJsonPath, JSON.stringify(appJson, null, 2));
  console.log(`   ‚úÖ ${fixes} corrections appliqu√©es et sauvegard√©es`);
}

// ============================================================================
// GENERATE REPORT
// ============================================================================
const report = {
  timestamp: new Date().toISOString(),
  version: appJson.version,
  drivers: appJson.drivers.length,
  issues: {
    total: issues.length,
    by_type: {}
  },
  fixes_applied: fixes,
  flow_cards: flowStats
};

// Count issues by type
issues.forEach(issue => {
  if (!report.issues.by_type[issue.type]) {
    report.issues.by_type[issue.type] = 0;
  }
  report.issues.by_type[issue.type]++;
});

fs.writeFileSync('./APP_JSON_VERIFICATION_REPORT.json', JSON.stringify(report, null, 2));

// ============================================================================
// FINAL SUMMARY
// ============================================================================
console.log('\n‚ïê'.repeat(80));
console.log('üìä R√âSUM√â V√âRIFICATION');
console.log('‚ïê'.repeat(80));

console.log(`\n‚úÖ STATS:`);
console.log(`   Drivers v√©rifi√©s: ${appJson.drivers.length}`);
console.log(`   Issues trouv√©es: ${issues.length}`);
console.log(`   Corrections appliqu√©es: ${fixes}`);

console.log(`\nüéØ ISSUES PAR TYPE:`);
Object.entries(report.issues.by_type).forEach(([type, count]) => {
  console.log(`   ${type}: ${count}`);
});

console.log(`\nüé® FLOW CARDS:`);
console.log(`   Triggers: ${flowStats.triggers}`);
console.log(`   Conditions: ${flowStats.conditions}`);
console.log(`   Actions: ${flowStats.actions}`);

console.log(`\nüìù FICHIERS CR√â√âS:`);
console.log(`   ‚úÖ APP_JSON_VERIFICATION_REPORT.json`);
if (fixes > 0) {
  console.log(`   ‚úÖ app.json (mis √† jour avec ${fixes} corrections)`);
}

console.log(`\nüöÄ PROCHAINES √âTAPES:`);
if (fixes > 0) {
  console.log(`   1. Valider: homey app validate`);
  console.log(`   2. Commit: git add . && git commit -m "fix: app.json verification and corrections"`);
  console.log(`   3. Push: git push origin master`);
} else {
  console.log(`   ‚úÖ Aucune correction n√©cessaire - app.json est parfait !`);
}

console.log(`\n${fixes > 0 ? '‚ö†Ô∏è' : '‚úÖ'}  V√âRIFICATION ${fixes > 0 ? 'COMPL√âT√âE AVEC CORRECTIONS' : 'R√âUSSIE'} !`);
