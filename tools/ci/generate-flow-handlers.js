#!/usr/bin/env node
/* eslint-disable no-console */
/**
 * generate-flow-handlers.js
 * =============================================================================
 * Auto-generates `registerRunListener` blocks for drivers whose `driver.flow.compose.json`
 * declares action/condition cards but whose `driver.js` is empty.
 *
 * Strategy:
 *   1. Scan all drivers/.../driver.flow.compose.json
 *   2. For each, if driver.js is < 200 bytes AND flow file has actions/conditions,
 *      generate a generic registerRunListener block matching the card IDs.
 *   3. The generated block calls the corresponding capability (action or getter).
 *   4. Dry-run by default; use --apply to write.
 *
 * Generated template (per driver):
 *
 *   'use strict';
 *   const { Driver } = require('homey');
 *
 *   class XxxDriver extends Driver {
 *     async onInit() {
 *       await super.onInit();
 *       if (this._flowCardsRegistered) return;
 *       this._flowCardsRegistered = true;
 *
 *       // Conditions
 *       this.homey.flow.getConditionCard('<id>')?.registerRunListener(async (args) => {
 *         if (!args.device) return false;
 *         return args.device.getCapabilityValue('<cap>') <op> <value>;
 *       });
 *
 *       // Actions
 *       this.homey.flow.getActionCard('<id>')?.registerRunListener(async (args) => {
 *         if (!args.device) return false;
 *         await args.device.setCapabilityValue('<cap>', <value>).catch(() => {});
 *         return true;
 *       });
 *     }
 *   }
 *   module.exports = XxxDriver;
 *
 * Usage:
 *   node tools/ci/generate-flow-handlers.js                  # dry-run
 *   node tools/ci/generate-flow-handlers.js --apply          # write to driver.js
 *   node tools/ci/generate-flow-handlers.js --driver=switch_1_gang  # single
 */

'use strict';

const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..', '..');
const DRIVERS = path.join(ROOT, 'drivers');

const ARGS = process.argv.slice(2);
const APPLY = ARGS.includes('--apply');
const SINGLE = (ARGS.find(a => a.startsWith('--driver=')) || '').split('=')[1] || null;
const VERBOSE = ARGS.includes('--verbose') || ARGS.includes('-v');
const PREVIEW = ARGS.includes('--preview');

/**
 * Map a flow card ID + args → capability setter/getter.
 * Returns { capability, op, value, type } or null if can't infer.
 */
function inferCard(card) {
  const id = card.id || '';
  const args = card.args || [];
  // Common patterns
  // turn_on / turn_off / toggle
  if (/turn_on|turn-on|on$/i.test(id) && !/on_gang|on_switch|on_button/.test(id)) {
    return { capability: 'onoff', value: true, type: 'action' };
  }
  if (/turn_off|turn-off|off$/i.test(id) && !/off_gang|off_switch|off_button/.test(id)) {
    return { capability: 'onoff', value: false, type: 'action' };
  }
  if (/toggle/i.test(id)) {
    return { capability: 'onoff', value: 'toggle', type: 'action' };
  }
  // set_dim / set_brightness / set_position
  const setMatch = id.match(/set_(\w+)$/);
  if (setMatch) {
    return { capability: setMatch[1], value: 'args', argName: args[0]?.name, type: 'action' };
  }
  // is_open / is_on / is_closed
  if (id.startsWith('is_') || id.includes('_is_')) {
    return { capability: id.replace(/^.*_is_/, ''), value: 'args', type: 'condition' };
  }
  // X_above / X_below
  const aboveBelow = id.match(/^(.+?)_(above|below|over|under)$/);
  if (aboveBelow) {
    return {
      capability: aboveBelow[1], op: aboveBelow[2] === 'above' || aboveBelow[2] === 'over' ? '>=' : '<=',
      value: 'args', type: 'condition',
    };
  }
  // battery / temperature / humidity / measure_X_changed
  const measureMatch = id.match(/^(measure_)?(\w+?)_(above|below|over|under|changed|low)$/);
  if (measureMatch) {
    const cap = measureMatch[1] ? `measure_${measureMatch[2]}` : measureMatch[2];
    return { capability: cap, type: 'condition' };
  }
  return null;
}

function buildHandlers(flowData) {
  const lines = [];
  lines.push("      // === AUTO-GENERATED FLOW HANDLERS ===");
  lines.push("      // Generated by tools/ci/generate-flow-handlers.js");
  lines.push("      // Edit if you need custom behaviour.");
  lines.push('');

  // Conditions
  for (const card of (flowData.conditions || [])) {
    const inferred = inferCard(card);
    const cardId = card.id;
    const handlerName = cardId.replace(/[^A-Za-z0-9]/g, '_');
    if (inferred && inferred.type !== 'condition') continue;
    if (inferred && inferred.capability) {
      const op = inferred.op || '===';
      const val = inferred.value === 'args' && (card.args || []).length > 0
        ? `args.${card.args[0].name}`
        : 'true';
      lines.push(`      this.homey.flow.getConditionCard('${cardId}')?.registerRunListener(async (args) => {`);
      lines.push(`        if (!args.device) return false;`);
      lines.push(`        return args.device.getCapabilityValue('${inferred.capability}') ${op} ${val};`);
      lines.push(`      });`);
    } else {
      // Generic: just check device exists
      lines.push(`      this.homey.flow.getConditionCard('${cardId}')?.registerRunListener(async (args) => {`);
      lines.push(`        if (!args.device) return false;`);
      lines.push(`        return true; // TODO: implement condition for ${cardId}`);
      lines.push(`      });`);
    }
  }

  // Actions
  for (const card of (flowData.actions || [])) {
    const inferred = inferCard(card);
    const cardId = card.id;
    if (inferred && inferred.type !== 'action') continue;
    if (inferred && inferred.capability && inferred.value !== 'toggle') {
      const val = inferred.value === 'args' && (card.args || []).length > 0
        ? `args.${card.args[0].name}`
        : inferred.value;
      lines.push(`      this.homey.flow.getActionCard('${cardId}')?.registerRunListener(async (args) => {`);
      lines.push(`        if (!args.device) return false;`);
      lines.push(`        await args.device.setCapabilityValue('${inferred.capability}', ${val}).catch(() => {});`);
      lines.push(`        return true;`);
      lines.push(`      });`);
    } else if (inferred && inferred.value === 'toggle') {
      lines.push(`      this.homey.flow.getActionCard('${cardId}')?.registerRunListener(async (args) => {`);
      lines.push(`        if (!args.device) return false;`);
      lines.push(`        const v = args.device.getCapabilityValue('${inferred.capability}');`);
      lines.push(`        await args.device.setCapabilityValue('${inferred.capability}', !v).catch(() => {});`);
      lines.push(`        return true;`);
      lines.push(`      });`);
    } else {
      lines.push(`      this.homey.flow.getActionCard('${cardId}')?.registerRunListener(async (args) => {`);
      lines.push(`        if (!args.device) return false;`);
      lines.push(`        return true; // TODO: implement action for ${cardId}`);
      lines.push(`      });`);
    }
  }

  return lines.join('\n');
}

function generateDriver(driverName, flowData) {
  const className = driverName
    .split(/[_-]/)
    .map(s => s.charAt(0).toUpperCase() + s.slice(1))
    .join('') + 'Driver';
  const handlers = buildHandlers(flowData);
  return `// Auto-generated by tools/ci/generate-flow-handlers.js — review before commit.
'use strict';

const { Driver } = require('homey');

class ${className} extends Driver {

  async onInit() {
    await super.onInit();
    if (this._flowCardsRegistered) return;
    this._flowCardsRegistered = true;

${handlers}
  }
}

module.exports = ${className};
`;
}

function processDriver(driverName) {
  const flowPath = path.join(DRIVERS, driverName, 'driver.flow.compose.json');
  const driverPath = path.join(DRIVERS, driverName, 'driver.js');
  if (!fs.existsSync(flowPath)) return { skipped: true, reason: 'no flow compose' };
  if (!fs.existsSync(driverPath)) return { skipped: true, reason: 'no driver.js' };
  const flowData = JSON.parse(fs.readFileSync(flowPath, 'utf8'));
  const driverJs = fs.readFileSync(driverPath, 'utf8');
  // Skip drivers that already have flow code
  if (driverJs.includes('registerRunListener')) return { skipped: true, reason: 'already has listener' };
  if (driverJs.length > 200) return { skipped: true, reason: 'driver.js too large' };
  const actions = (flowData.actions || []).length;
  const conditions = (flowData.conditions || []).length;
  if (actions === 0 && conditions === 0) return { skipped: true, reason: 'no actions/conditions' };
  const generated = generateDriver(driverName, flowData);
  return { skipped: false, generated, actions, conditions };
}

function main() {
  const drivers = fs.readdirSync(DRIVERS).filter(d => {
    try { return fs.statSync(path.join(DRIVERS, d)).isDirectory(); } catch { return false; }
  });
  let processed = 0;
  let skipped = 0;
  let errors = 0;
  const targets = SINGLE ? [SINGLE] : drivers;
  for (const d of targets) {
    try {
      const r = processDriver(d);
      if (r.skipped) {
        skipped++;
        if (VERBOSE) console.log(`⏭  ${d}: ${r.reason}`);
        continue;
      }
      if (PREVIEW) {
        console.log(`=== ${d} ===`);
        console.log(r.generated);
        console.log('');
        processed++;
        continue;
      }
      if (APPLY) {
        fs.writeFileSync(path.join(DRIVERS, d, 'driver.js'), r.generated);
        console.log(`✅ ${d}: ${r.actions} actions / ${r.conditions} conditions — driver.js generated`);
      } else {
        console.log(`[DRY] ${d}: would generate ${r.actions} actions / ${r.conditions} conditions`);
      }
      processed++;
    } catch (e) {
      console.error(`❌ ${d}: ${e.message}`);
      errors++;
    }
  }
  console.log(`\n${APPLY ? '✅ Applied' : '🔍 Dry-run'}: ${processed} drivers, ${skipped} skipped, ${errors} errors`);
  if (!APPLY && processed > 0) {
    console.log('Run with --apply to write driver.js files.');
  }
}

main();
