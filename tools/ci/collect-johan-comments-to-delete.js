#!/usr/bin/env node
/**
 * collect-johan-comments-to-delete.js
 *
 * Extracts ALL comment IDs from JohanBendz/com.tuya.zigbee
 * posted by dlnraja OR by our bots (Mavis, github-actions, etc.)
 *
 * Output:
 *   - .github/state/johan-comments-to-delete.json (with comment IDs, issue refs, URLs)
 *   - .github/state/johan-comments-to-delete.csv (for quick review)
 *   - tools/ci/delete-johan-comments.sh (bash script with gh API calls)
 *
 * This is the LIST only — actual deletion requires GH_TOKEN with write access
 * and is done via the bash script (user runs manually with their own token).
 *
 * @author Mavis P10 — cleanup
 */

'use strict';

const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..', '..');
const AUDIT = path.join(ROOT, '.diag', 'johan-shadow-comments-audit.json');
const ENRICHED = path.join(ROOT, 'data', 'community-sync', 'johanbendz-issues-enriched.json');
const OUT_JSON = path.join(ROOT, '.github', 'state', 'johan-comments-to-delete.json');
const OUT_CSV = path.join(ROOT, '.github', 'state', 'johan-comments-to-delete.csv');
const OUT_SCRIPT = path.join(ROOT, 'tools', 'ci', 'delete-johan-comments.sh');

const OUR_AUTHORS = [
  'dlnraja',
  'Mavis',
  'mavis',
  'github-actions[bot]',
  'tuya-triage-bot',
  'diag-resolver',
  'auto-responder',
  'dln-bot',
];

function isOurs(comment) {
  if (!comment || !comment.author) return false;
  const a = (comment.author || '').toLowerCase();
  return OUR_AUTHORS.some(t => a.includes(t.toLowerCase()));
}

function extractComments(audit) {
  const out = [];
  // 1. From issueSignals — main path
  for (const sig of audit.issueSignals || []) {
    // All comments in the issue (we need to filter by author)
    const all = [];
    if (sig.latest) all.push(sig.latest);
    if (sig.latestExplicitSignal) all.push(sig.latestExplicitSignal);
    if (Array.isArray(sig.ownIssueComments)) all.push(...sig.ownIssueComments);
    if (Array.isArray(sig.reviewComments)) all.push(...sig.reviewComments);
    // From fingerprints
    for (const fp of sig.fingerprints || []) {
      if (Array.isArray(fp.signals)) all.push(...fp.signals);
    }
    for (const c of all) {
      if (isOurs(c) && c.commentId) {
        out.push({
          commentId: c.commentId,
          issueNumber: sig.issueNumber,
          author: c.author,
          url: c.url,
          createdAt: c.createdAt,
          updatedAt: c.updatedAt,
          signal: c.signal,
          excerpt: (c.excerpt || '').substring(0, 100),
        });
      }
    }
  }
  return out;
}

function main() {
  console.log('Johan Comments Cleanup — Collect\n');
  const audit = JSON.parse(fs.readFileSync(AUDIT, 'utf8'));
  const enriched = JSON.parse(fs.readFileSync(ENRICHED, 'utf8'));

  const comments = extractComments(audit);
  console.log('Total OUR comments to delete:', comments.length);

  // Group by issue
  const byIssue = {};
  for (const c of comments) {
    if (!byIssue[c.issueNumber]) byIssue[c.issueNumber] = [];
    byIssue[c.issueNumber].push(c);
  }
  console.log('Issues touched:', Object.keys(byIssue).length);

  // Group by author
  const byAuthor = {};
  for (const c of comments) {
    if (!byAuthor[c.author]) byAuthor[c.author] = 0;
    byAuthor[c.author]++;
  }
  console.log('By author:', byAuthor);

  // Save JSON
  fs.mkdirSync(path.dirname(OUT_JSON), { recursive: true });
  fs.writeFileSync(OUT_JSON, JSON.stringify({
    timestamp: new Date().toISOString(),
    target: 'JohanBendz/com.tuya.zigbee',
    fork: 'dlnraja/com.tuya.zigbee',
    ourAuthors: OUR_AUTHORS,
    summary: {
      totalComments: comments.length,
      issuesTouched: Object.keys(byIssue).length,
      byAuthor,
    },
    comments,
  }, null, 2));
  console.log('\n✓ JSON:', OUT_JSON);

  // Save CSV
  const csvLines = ['commentId,issueNumber,author,createdAt,url,excerpt'];
  for (const c of comments) {
    const esc = (s) => '"' + (s || '').toString().replace(/"/g, '""') + '"';
    csvLines.push([
      c.commentId, c.issueNumber, esc(c.author), c.createdAt || '',
      c.url, esc(c.excerpt),
    ].join(','));
  }
  fs.writeFileSync(OUT_CSV, csvLines.join('\n'));
  console.log('✓ CSV:', OUT_CSV);

  // Generate bash script
  let script = '#!/usr/bin/env bash\n';
  script += '# delete-johan-comments.sh\n';
  script += '# Generated by collect-johan-comments-to-delete.js\n';
  script += '# DELETES all dlnraja/bot comments from JohanBendz/com.tuya.zigbee\n';
  script += '#\n';
  script += '# Requirements:\n';
  script += '#   - gh CLI installed\n';
  script += '#   - GH_TOKEN env var with repo scope\n';
  script += '#   - OR set GITHUB_TOKEN env var\n';
  script += '#\n';
  script += '# Safety:\n';
  script += '#   - DRY_RUN=true (default): only shows what would be deleted\n';
  script += '#   - DRY_RUN=false: actually deletes\n';
  script += '#   - Use --confirm flag to skip the safety prompt\n';
  script += '#\n';
  script += '# Usage:\n';
  script += '#   DRY_RUN=true bash delete-johan-comments.sh          # preview\n';
  script += '#   DRY_RUN=false bash delete-johan-comments.sh --confirm  # delete\n';
  script += '\n';
  script += 'set -e\n';
  script += '\n';
  script += 'REPO="JohanBendz/com.tuya.zigbee"\n';
  script += 'DRY_RUN="${DRY_RUN:-true}"\n';
  script += 'CONFIRM=false\n';
  script += 'for arg in "$@"; do\n';
  script += '  if [ "$arg" = "--confirm" ]; then CONFIRM=true; fi\n';
  script += 'done\n';
  script += '\n';
  script += 'if [ "$DRY_RUN" = "false" ] && [ "$CONFIRM" != "true" ]; then\n';
  script += '  echo "ERROR: To actually delete, you MUST pass --confirm"\n';
  script += '  echo "       DRY_RUN=true bash delete-johan-comments.sh       (preview)"\n';
  script += '  echo "       DRY_RUN=false bash delete-johan-comments.sh --confirm  (delete)"\n';
  script += '  exit 1\n';
  script += 'fi\n';
  script += '\n';
  script += 'if [ -z "$GH_TOKEN" ] && [ -z "$GITHUB_TOKEN" ]; then\n';
  script += '  echo "ERROR: GH_TOKEN or GITHUB_TOKEN must be set"\n';
  script += '  echo "       export GH_TOKEN=ghp_***your_token***"\n';
  script += '  exit 1\n';
  script += 'fi\n';
  script += '\n';
  script += 'TOKEN="${GH_TOKEN:-$GITHUB_TOKEN}"\n';
  script += 'echo "Target: $REPO"\n';
  script += 'echo "Mode: $([ "$DRY_RUN" = "true" ] && echo PREVIEW || echo DELETE)"\n';
  script += 'echo "Comments to process: ' + comments.length + '"\n';
  script += 'echo ""\n';
  script += '\n';
  script += 'DELETED=0\n';
  script += 'FAILED=0\n';
  script += 'SKIPPED=0\n';
  script += '\n';
  for (const c of comments) {
    const safeExcerpt = (c.excerpt || '').substring(0, 60).replace(/'/g, "\\'");
    script += '# ' + c.author + ' on #' + c.issueNumber + ' (' + c.createdAt + ')\n';
    script += 'if [ "$DRY_RUN" = "true" ]; then\n';
    script += '  echo "  [DRY-RUN] Would delete comment ' + c.commentId + ' by ' + c.author + ' on #' + c.issueNumber + '"\n';
    script += '  SKIPPED=$((SKIPPED + 1))\n';
    script += 'else\n';
    script += '  if curl -sS -X DELETE \\\n';
    script += '       -H "Authorization: Bearer $TOKEN" \\\n';
    script += '       -H "Accept: application/vnd.github+json" \\\n';
    script += '       -H "X-GitHub-Api-Version: 2022-11-28" \\\n';
    script += '       "https://api.github.com/repos/$REPO/issues/comments/' + c.commentId + '" \\\n';
    script += '       -o /dev/null -w "%{http_code}" > /tmp/johan-del-' + c.commentId + '.code 2>/dev/null; then\n';
    script += '    CODE=$(cat /tmp/johan-del-' + c.commentId + '.code)\n';
    script += '    rm -f /tmp/johan-del-' + c.commentId + '.code\n';
    script += '    if [ "$CODE" = "204" ]; then\n';
    script += '      echo "  ✓ Deleted comment ' + c.commentId + ' on #' + c.issueNumber + '"\n';
    script += '      DELETED=$((DELETED + 1))\n';
    script += '    elif [ "$CODE" = "404" ]; then\n';
    script += '      echo "  - Already gone: ' + c.commentId + '"\n';
    script += '      SKIPPED=$((SKIPPED + 1))\n';
    script += '    else\n';
    script += '      echo "  ✗ Failed ' + c.commentId + ' on #' + c.issueNumber + ' (HTTP $CODE)"\n';
    script += '      FAILED=$((FAILED + 1))\n';
    script += '    fi\n';
    script += '  fi\n';
    script += '  sleep 0.3  # rate limit protection\n';
    script += 'fi\n';
  }
  script += '\n';
  script += 'echo ""\n';
  script += 'echo "=== SUMMARY ==="\n';
  script += 'echo "Deleted: $DELETED"\n';
  script += 'echo "Failed : $FAILED"\n';
  script += 'echo "Skipped: $SKIPPED"\n';
  script += '\n';
  script += 'if [ "$DRY_RUN" = "true" ]; then\n';
  script += '  echo ""\n';
  script += '  echo "To actually delete, run:"\n';
  script += '  echo "  DRY_RUN=false bash delete-johan-comments.sh --confirm"\n';
  script += 'fi\n';

  fs.writeFileSync(OUT_SCRIPT, script, { mode: 0o755 });
  console.log('✓ Bash script:', OUT_SCRIPT);

  // Show first 10
  console.log('\n=== FIRST 10 COMMENTS TO DELETE ===');
  for (const c of comments.slice(0, 10)) {
    console.log('  #' + c.issueNumber + ' by ' + c.author + ' (' + c.createdAt + '): ' + c.excerpt);
  }
  if (comments.length > 10) console.log('  ... and ' + (comments.length - 10) + ' more');
}

if (require.main === module) main();
